int DestroyRasterFont(RASTERFONT * rasterFont)
{
	for (int i=0;i<128;i++)	{
		if (rasterFont->c[i])	{
			if (rasterFont->c[i]->pixelarray)	{
				free(rasterFont->c[i]->pixelarray);
			}
			free(rasterFont->c[i]);
		}
	}

	return 1;
}

